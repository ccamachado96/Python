#Calculando a média aritmética de um aluno - Feito em 05/05/2024 - ÀS 20H18

n1 = float(input('Informe a nota do aluno: '))
n2 = float(input('Informe a segunda nota do aluno: '))

print('A primeira nota informada foi: {:.1f}'.format(n1))
print('A segunda nota informada foi: {:.1f}'.format(n2))

media = (n1 + n2) / 2

print('A média do aluno foi de: {:.1f}'.format(media))

